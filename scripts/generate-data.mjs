const dataset = buildDatasets();
fs.writeFileSync(outThaiPath, serializeThai(dataset));
fs.writeFileSync(outEnglishPath, serializeEnglish(dataset));
if (fs.existsSync(legacyOutPath)) fs.rmSync(legacyOutPath);

console.log(
  `wrote ${dataset.rows.length} rows (${dataset.sourceCounts.provinces} provinces, ${dataset.sourceCounts.districts} districts, ${dataset.sourceCounts.subdistricts} subdistricts from source)`
);
console.log(`- ${path.relative(root, outThaiPath)}`);
console.log(`- ${path.relative(root, outEnglishPath)}`);
